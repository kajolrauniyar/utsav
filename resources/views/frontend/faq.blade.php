                            <p><strong>Day 01: Arrival Day in Kathmandu&nbsp;</strong></p>

                            <p style="display: none;">Altitude: 1350m/4428ft&nbsp;<br>
                            Welcome to the land of Himalayas! On arrival in Tribhuvan International Airport in Kathmandu; Advanced Adventures Trek Guide will great you then transfer you to our trip hotel. Check in to hotel and free time for leisure. Late afternoon, we meet again and brief your upcoming Annapurna Base Camp Trek with us and introduce our trekking team. Evening, we host welcome dinner (Eating Nepali Food) at one of the finest local restaurant with music and ethnic dance. Overnight Kathmandu.&nbsp;</p>

                            <p><strong>Day 02: Drive from Kathmandu to Pokhara by Deluxe Tourist Bus&nbsp;</strong></p>

                            <p style="display: none;">Altitude: 820m/2510 ft. | Distance of driving (210kms)- 6 to 7 hours of driving&nbsp;<br>
                                With early morning breakfast at&nbsp;hotel, we then drive to tourist Bus Park for our drive to Pokhara with deluxe tourist bus. This drive wills takes 5 to 6 hours that involves driving through local hills, natural and landscapes areas including with riverside in Trishuli River, which is famous for day white water rafting. This drive will be mostly though the highway where traffic could be a lot, thus we will have a slow and safe drive.&nbsp;<br>
                                <br>
                            At middle of the way, we will stop for Lunch, which is inclusive in bus drive. On arrival in Pokhara, transfer to our hotel. Now, you will have free afternoon for your personal exploration in wonderful Pokhara Valley, Fewa Lake, and Lakeside markets. O/N at Pokhara.&nbsp;</p>

                            <p><strong>Day 03: Drive from Pokhara to Nayapul (Trekking starting point) &amp; Trek to Tikhadhunga&nbsp;</strong></p>

                            <p style="display: none;">Altitude: 1,540m/5,052 ft. | Distance of driving: 1 hr. drive | Distance of walking: (10 kms)- 3-4 hrs. Walks&nbsp;<br>
                            Morning breakfast at Pokhara hotel, then prepare to drive to Nayapul that continues along with the great scenery passing through several countryside villages. From Nayapul, we start our adventure inAnnapurna region. The trek now continue to the warm sub tropical region and follows the Modi River to Birethanti where you can see the confluence of the two river of Modi and Bhurungdi Khola. Here at Birethanti, we will have to check our National Park Permit, and then continue gentle walking onward to Bhurungdi Khola that have great landscapes, water fall, high green forest and local villages till reaching to Tikhedhunga where we will have our overnight stay in Mt. Lodge. You will have free evening for personal visits in local villages and riverside areas as per your interest. O/N at Mt. Lodge.&nbsp;</p>

                            <p><strong>Day 04: Trek from Tikhedhunga to Ghorepani&nbsp;</strong></p>

                            <p style="display: none;">Altitude: 2,800m/9,186ft | Distance of walking: (12-14 kms) 5-6 hrs. Walks&nbsp;<br>
                            Morning we prepare for the long uphill and start with steeply ascends on the long stone steps to Ulleri village with the amazing landscape and Annapurna Himalayan scenery. This scenery gives you inner power to climb up these steps and immerse you to walk more. After few hours of long hard climb with several breaks in between reaching at Ulleri village, from here the path is gradual on the winding trail through the cool shade of forest and stream sound, we continues walk in the midst of the giant rhododendron forest and finally reaches you at Ghorepani for overnight stop. Ghorepani Deurali is a busy spot on the ridge in the midst of the beautiful rhododendron forest, offering fantastic views of Dhaulagiri and Annapurna Himalayan range. Overnight at Ghorepani.</p>

                            <p><strong>Day 05: Morning- Hike to Poon Hill for Annapurna Sunrise View &amp; Trek to Tadapani at Afternoon&nbsp;</strong></p>

                            <p style="display: none;">Altitude: At Poon hill 3,210m/10,531ft &amp; at Tadapani 2650m/8694ft | Distance of walking: (14-16 kms) 6-7 hrs. Walks&nbsp;<br>
                            Morning, we start our hike up to Poon Hill, one of the panoramicviewpoint of this Annapurna. En-route offering the glimpse of aurora walking through the pleasant rhododendron forest. Pun hill named after the hill tribe Pun, inhabitants of Ghorepani/Shikha village. This is wide range of panoramic scenery from the far western Himalaya of Dolpo, Mt. Dhaulagiri, and Annapurna including Mt. Machhapuchre (Fish Tail) and all the way to Manasalu Himalayan range. It is picturesque Himalayan scenery and good memorable moments spending an hour to the top. After a great time return back to Ghorepani for breakfast and continue trek to onward destination, the walk passes through the Rhododendron, Pine and fir forest with up and downs hill and finally you will reaches at Tadapani for overnight. This is a great place surrounding by rhododendron forest with closest view of Mt. Fish Tail (6993m), Annapurna South (7219m) &amp; Hiunchuli (6441m). You will have impressive sunset views of Annapurna’s and Fishtail from here. O/N at Mt. Lodge.&nbsp;</p>

                            <p><strong>Day 06: Trek from Tadapani to Chhomrong&nbsp;</strong></p>

                            <p style="display: none;">Altitude: 1,950m/6,397 ft. | Distance of walking: (12 kms)- 5-6 hrs. Walks&nbsp;<br>
                            With wonderful morning with sunrise views in Fishtail &amp; AnnapurnaSouth, and breakfast in Tadapani, now we will prepare to walk steeply downhill to the Kimrong Khola through clean forest of oaks and rhododendrons with wonderful landscapes views of the valley. On reaching to riverside, across the river through suspension bridge, then the trail follows up to the Gurjung village for your lunch stop. Further trek leads uphill for half an hour, once at the top of the ridge, then the walk is fairly easy all the way to village of Chhomrong the last village local settlement and the gateway to the Annapurna Base Camp. Chhomrong is one of the two biggest villages of this region, which is a inhabitant of the Gurung Peoples. Landscape views here are impressive. O/N at Mt. Lodge.&nbsp;</p>

                            <p><strong>Day 07: Trek from Chhomrong Village to </strong><strong>Dovan</strong><strong>&nbsp;</strong></p>

                            <p style="display: none;">Altitude: 2,900m/9,514 ft. | Distance of walking: (12 kms)- 6-7 hrs.<br>
                                Walks&nbsp;From Chhomrong, trail leads to the long terrace village &amp; farm fields as you descent to the Chhomrong Khola, after the suspension bridge cross, the trail uphill most of the way for 2 hours till to Sinuwa Hill, then it continue gentle walking through the forested valley to Bamboo which is a place in the midst of the bamboo and rhododendron and oaks forest withcouple of Mt. Lodges. Depending on your interest, you could have lunch at Sinuwa or in Bamboo.&nbsp;<br>
                                <br>
                            From here, an hour walk on the gradual uphill-forested path brings us toDovan, which is a small area with only a few guesthouses for overnight. As Dovan is near the Modi River side, and in forested hill, it has nice feel of nature. O/N at Mt. Lodge.&nbsp;</p>

                            <p><strong>Day 08: Trek from Dovan- Machhapuchre Base Camp (MBC)&nbsp;</strong></p>

                            <p style="display: none;">Altitude: 3700m/, 12136 ft. | Distance of walking: 10-12 kms)- 4 to 5 hrs. Walks&nbsp;<br>
                            The trail continues for about 2 hrs from here the route winds up steeply to a bank of rock and then level out, running through thickets of Bamboo at the bottom of the gorge, keeping always on the west side of the river, then reaching to a small shrine at Panchen Barah, from here the trail climbs to Himalaya Hotel. It’s about an hour walk, first on a rocky trail through forests then up to the Hinku cave, where from the trail crosses a ravine and major avalanche track just further till Deurali (3,000m/9,840 ft.). From here, the trail continues into the sanctuary, it crosses two wide avalanche tracks on a narrow trail that huddles up against the cliffs and get Bagar (Riverside). After here climb across more avalanche paths cross a moraine, then climb towards to open little valley that is known as Machhapuchre Base Camp (MBC). Till here, we were walking invalley between two high hills, but now the valley opens up and great views of Fishtail and Annapurna can be seen very closely. Free time for visit to view point hill or simply rest at Lodge. O/N at Mt. Lodge.&nbsp;</p>

                            <p><strong>Day 09: Trek from MBC to Annapurna Base Camp (ABC)&nbsp;</strong></p>

                            <p style="display: none;">Altitude: 4130m, 13582 ft. | Distance of walking: (6kms)1-2 hrs walks<br>
                            Today, we will have short day trek to Annapurna Base Camp. With easy morning and breakfast in MBC, prepare to walk to Annapurna Base Camp. The trail follows uphill dry and deserted part of the mountain to Annapurna Base Camp, which could be windy as well. Panorama Views of Annapurna’s are amazing en-route. You will have full day here in Annapurna Base Camp. Enjoy your short walking in base camp areas, hill and glacier sites as per your interest. A sunset view from ABC is spectacular. O/N at Mt. Lodge.&nbsp;</p>

                            <p><strong>Day 10: Morning Explore ABC- Annapurna Base Camp (ABC) to Bamboo&nbsp;</strong></p>

                            <p style="display: none;">Altitude: 2,300m/7,544 ft. Distance of walking: (12-14 kms) -5-6- hrs. Walks&nbsp;<br>
                                Today, you will havegreat morning with sunrise in Annapurna Base Camp. Views of Annapurna South, Hiunchuli, Fishtail, Tent Peak, Annapurna I, and other Annapurna peaks at sunrise are just incredible. Enjoy this wonderful morning at ABC.&nbsp;<br>
                                <br>
                            After breakfast, now we will now prepare to walk back to Bamboo at this afternoon. It would be a gentle trekking back through the same trail that we walked uphill and as it is complete descend so walking will be quicker. O/N at Mt. Lodge.</p>

                            <p><strong>Day 11: Trek from Bamboo to Jhinu Danda&nbsp;</strong></p>

                            <p style="display: none;">Altitude: 1,780m/5,859 ft. Distance of walking: (12-14 kms)- 5-6 hrs. Walks&nbsp;<br>
                                Today again, we will trek back to Chhomrongat the same trail. With pleasant walk to Sinuwa, trek downhill to Chhomrong Khola, and then step climb to Chhomrong Village for lunch. From Chhomrong, we will have a deep descend hill walk to Jhinu Danda.&nbsp;<br>
                                <br>
                            Jhunu Danda is natural and landscapes areas withsettlement of locals in the areas. We will have opportunity to visit natural hot spring in Jhinu for natural hot water bath, and relax at hot spring, which is along the riverside with great natural areas. Return back to lodge in evening and O/N at Mt. Lodge.&nbsp;</p>

                            <p><strong>Day 12: Trek from Jhinu Danda to Kyumi &amp; Drive to Pokhara&nbsp;</strong></p>

                            <p style="display: none;">Altitude: 823m/2,700ft | Distance of walking: (10-12 kms)- 3 to 4 hrs. Walks Distance of driving: 2 hrs.&nbsp;<br>
                                Today, our trek will end. You will have free and easy morning in Jhinu Danda. After breakfast, we will then prepare our walking through the Modi Rivervalley. Walking involves mostly through riverside, natural forested areas, farming lands and villages. On reaching to Kyumi, we will take a drive to Nayapul, and then to Pokhara, which takes about 2 hours natural drive.&nbsp;<br>
                                <br>
                            Free afternoon in Pokhara for personal shopping or relax. In Pokhara, you will also have opportunity to do activity of Paragliding, Zip Flyer, Micro light flight etc. O/N at Hotel</p>

                            <p><strong>Day 13: Drive from Pokhara to Kathmandu&nbsp;</strong></p>

                            <p style="display: none;">Distance of Driving: (210kms) 5-6 hrs. Drives&nbsp;<br>
                            Today, we will drive back to Kathmandu withtourist bus. After morning breakfast at hotel in Pokhara, transfer you to Bus Park for Kathmandu Bus. This bus drive will be 5 to 6 hours slow drive in natural and landscapes and riverside areas. Lunch will be served by Bus in a middle of the journey.&nbsp;Reaching in Kathmandu, transfer to hotel. In evening, we host farewell dinner at one of the finest restaurant in town. O/N at Kathmandu&nbsp;</p>

                            <p><strong>Day 14: Final Departure from Kathmandu&nbsp;</strong></p>

                            <p style="display: none;">Morning breakfast and free time onward. If you staying longer in Kathmandu or interested for other activities with us, we will be very pleased to assist you to this. If you departing, we will transfer you to airport and our trip ends.&nbsp;<br>
                                <br>
                                <strong>End of our services!&nbsp;</strong></p>

